/**
 * Export interview guide as PDF using print dialog
 * @param {Object} interviewData - The interview data
 * @param {Object} repoData - Repository data
 */
export const exportToPdf = (interviewData, repoData) => {
  const html = generatePrintableHTML(interviewData, repoData);
  
  // Create a new window for printing
  const printWindow = window.open('', '_blank');
  if (!printWindow) {
    throw new Error('Please allow popups to export PDF');
  }
  
  printWindow.document.write(html);
  printWindow.document.close();
  
  // Wait for content to load, then print
  printWindow.onload = () => {
    printWindow.focus();
    printWindow.print();
  };
};

/**
 * Export as JSON file
 * @param {Object} interviewData - The interview data
 * @param {Object} repoData - Repository data
 */
export const exportToJson = (interviewData, repoData) => {
  const data = {
    repository: repoData,
    generatedAt: new Date().toISOString(),
    ...interviewData,
  };
  
  const blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${repoData.repo}_interview_data.json`;
  a.click();
  URL.revokeObjectURL(url);
};

export default exportToPdf;
